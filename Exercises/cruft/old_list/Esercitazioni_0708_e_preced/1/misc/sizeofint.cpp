#include <iostream>
using namespace std;

main() {
    cout << " Number of bytes in short        = " << sizeof(short) << endl;
    cout << " Number of bytes in long         = " << sizeof(long) << endl;
    cout << " Number of bytes in int          = " << sizeof(int) << endl;
    cout << " Number of bytes in unsigned int = " << sizeof(unsigned int) << endl;
}
